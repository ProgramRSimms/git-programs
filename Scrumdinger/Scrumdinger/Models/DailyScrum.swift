        DailyScrum(title: "Appointment", attendees: ["Abe", "Robert", "Jenny"], lengthInMinutes: 60, theme: .orange),
        DailyScrum(title: "NO SHOW", attendees: ["Abe", "Robert", "Jenny"], lengthInMinutes: 120, theme: .poppy)
    ]
}
